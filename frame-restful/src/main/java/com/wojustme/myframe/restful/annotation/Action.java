/**
 * Action方法注解 -> 方法级别
 * 用于拦截每次HTTP请求
 */
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.RUNTIME)
public @interface Action {
	// 路径
	String url();
	// 请求方法类型
	ReqMethod method() default ReqMethod.GET;
}
